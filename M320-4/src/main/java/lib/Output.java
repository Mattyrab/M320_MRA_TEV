package lib;

/**
 * Utility class to for outputting data to the console.
 *
 * @author Matthew Rabe
 */
public final class Output {

    private Output(){}

    /**
     * Shortened method to print message to the console.
     * @param message String containing the message to print to the console
     */
    public static void printMessage(String message) {
        System.out.println(message);
    }
}
